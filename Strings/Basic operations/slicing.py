#slicing : accessing part of string
#str[starting idx:ending idx] ending index is not included
str="simultaneously"
print(str[1:4])#ending indexing is not included
print(str[0:5])
print(str[5:len(str)])
print(str[0:len(str)])
print(str[0:])#print up to end
print(str[0:14])
print(str[:14])#take from start i.e 0 to end


